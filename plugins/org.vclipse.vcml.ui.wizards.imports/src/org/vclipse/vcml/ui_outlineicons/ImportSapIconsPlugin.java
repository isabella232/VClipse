package org.vclipse.vcml.ui_outlineicons;

import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class ImportSapIconsPlugin extends AbstractUIPlugin {

	public static final String PLUGIN_ID = "org.vclipse.vcml.ui_outlineicons"; //$NON-NLS-1$
	
	private static ImportSapIconsPlugin plugin;
	
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	public static ImportSapIconsPlugin getDefault() {
		return plugin;
	}
}
